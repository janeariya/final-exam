public class Product {
    private int productA;
    private int productB;
    private int productC;
    private int productD;
    private int productE;

    public Product(int productA, int productB, int productC, int productD, int productE) {
        this.productA = productA;
        this.productB = productB;
        this.productC = productC;
        this.productD = productD;
        this.productE = productE;
    }

    public int getProductA() {
        return productA;
    }

    public void setProductA(int productA) {
        this.productA = productA;
    }

    public int getProductB() {
        return productB;
    }

    public void setProductB(int productB) {
        this.productB = productB;
    }

    public int getProductC() {
        return productC;
    }

    public void setProductC(int productC) {
        this.productC = productC;
    }

    public int getProductD() {
        return productD;
    }

    public void setProductD(int productD) {
        this.productD = productD;
    }

    public int getProductE() {
        return productE;
    }

    public void setProductE(int productE) {
        this.productE = productE;
    }
}
